const QUARTERS = ["Q1", "Q2", "Q3", "Q4"];

function fillSubject(
  template: string,
  rand: () => number,
  firm: string
): string {
  return template
    .replace("{topic}", TOPICS[Math.floor(rand() * TOPICS.length)])
    .replace("{quarter}", QUARTERS[Math.floor(rand() * QUARTERS.length)])
    .replace("{firm}", firm);
}

function genSamples(
  seed: number,
  ownerName: string,
  firmName: string,
  count: number,
  baseISO: string,
  templates: string[]
): ActivitySample[] {
  if (count <= 0) return [];
  const rand = mulberry32(seed);
  const baseMs = new Date(baseISO).getTime();
  const max = Math.min(count, 5);
  const out: ActivitySample[] = [];
  for (let i = 0; i < max; i++) {
    const tmpl = templates[Math.floor(rand() * templates.length)];
    const ts = new Date(
      baseMs -
        i * 2.5 * 24 * 3600 * 1000 -
        Math.floor(rand() * 8) * 86400000
    );
    out.push({
      subject: fillSubject(tmpl, rand, firmName),
      timestamp: ts.toISOString(),
      owner: ownerName,
    });
  }
  return out;
}

function makeSamples(
  seedOffset: number,
  ownerName: string,
  firmName: string,
  associations: {
    emails: number;
    calls: number;
    meetings: number;
    notes: number;
    tasks: number;
  },
  baseISO: string
): CompanyActivitySamples {
  return {
    emails: genSamples(
      seedOffset + 1,
      ownerName,
      firmName,
      associations.emails,
      baseISO,
      EMAIL_SUBJECTS
    ),
    calls: genSamples(
      seedOffset + 2,
      ownerName,
      firmName,
      associations.calls,
      baseISO,
      CALL_SUBJECTS
    ),
    meetings: genSamples(
      seedOffset + 3,
      ownerName,
      firmName,
      associations.meetings,
      baseISO,
      MEETING_SUBJECTS
    ),
    notes: genSamples(
      seedOffset + 4,
      ownerName,
      firmName,
      associations.notes,
      baseISO,
      NOTE_SUBJECTS
    ),
    tasks: genSamples(
      seedOffset + 5,
      ownerName,
      firmName,
      associations.tasks,
      baseISO,
      TASK_SUBJECTS
    ),
  };
}

